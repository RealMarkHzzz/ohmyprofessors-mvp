export function getAllProfessors(): Professor[] {
  return mockProfessors;
}

/**
 * Get professor by slug
 */
export function getProfessorBySlug(slug: string): Professor | undefined {
  return mockProfessors.find(prof => prof.slug === slug);
}

/**
 * Get professors by department
 */
export function getProfessorsByDepartment(department: string): Professor[] {
  return mockProfessors.filter(prof => prof.department === department);
}

/**
 * Search professors by name or course
 */
export function searchProfessors(query: string): Professor[] {
  const lowerQuery = query.toLowerCase();
  return mockProfessors.filter(prof => 
    prof.name.toLowerCase().includes(lowerQuery) ||
    prof.department.toLowerCase().includes(lowerQuery) ||
    prof.email.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get top-rated professors
 */
export function getTopProfessors(limit: number = 10): Professor[] {
  return [...mockProfessors]
    .sort((a, b) => b.overall_rating - a.overall_rating)
    .slice(0, limit);
}

/**
 * Get all unique departments
 */
export function getAllDepartments(): string[] {
  return Array.from(new Set(mockProfessors.map(prof => prof.department))).sort();
}

/**
 * Get all unique tags
 */
export function getAllTags(): string[] {
  const tagsSet = new Set<string>();
  mockProfessors.forEach(prof => {
    prof.tags.forEach(tag => tagsSet.add(tag));
  });
  return Array.from(tagsSet).sort();
}

